package operator;

public class Demo3 {
    public static void main(String[] args) {
        double a = 9.5;
        double b = 520;
        a += b;
        System.out.println(a);

        double i = 600;
        double j = 520;
        i -= j;
        System.out.println(i);

        int m=10;
        int n=5;
        m*=n;
        System.out.println(m);
        m/=n;
        System.out.println(m);
        m%=n;
        System.out.println(m);
    }
}
